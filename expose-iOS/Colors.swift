import UIKit

class Colors{
    
    static func returnGreenExpose() -> UIColor{
        return UIColor(red: 93/255, green: 192/255, blue: 155/255, alpha: 1)
    }
    
    static func returnBackgroundGrey() -> UIColor{
        return UIColor(red: 233/255, green: 233/255, blue: 233/255, alpha: 1)
    }
}
